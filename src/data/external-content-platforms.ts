export function getExternalPlatform(id: string) {
  return externalContentPlatforms.find((p) => p.id === id);
}

export function getRecommendedTeaserStyles(platformId: string): TeaserStyle[] {
  return getExternalPlatform(platformId)?.teaserStyles ?? ["question-hook"];
}

export function getActiveAdapterPlatforms() {
  return externalContentPlatforms.filter((p) => p.adapterStatus === "active");
}

export function getManualPlatforms() {
  return externalContentPlatforms.filter(
    (p) => p.adapterStatus === "manual" || p.id === "manual-export",
  );
}
