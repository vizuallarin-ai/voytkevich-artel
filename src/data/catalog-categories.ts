export function getCategoryBySlug(slug: string) {
  return catalogCategories.find((c) => c.slug === slug);
}

export function getRelatedCategories(slug: string): CatalogCategory[] {
  const cat = getCategoryBySlug(slug);
  if (!cat) return [];
  return cat.relatedCategories
    .map((s) => getCategoryBySlug(s))
    .filter((c): c is CatalogCategory => !!c);
}

export function categoryHref(category: CatalogCategory): string {
  return category.queryHref ?? `/catalog/kategoriya/${category.slug}`;
}
